#!/usr/bin/env node
/**
 * Scaffold a mockup flow directory with mockup.json, input.css, and placeholder index.html.
 * Usage: node scaffold-mockup-flow.js --name auth-flow --output-dir docs/design-mockups/ --pages "login,register"
 */

const fs = require('fs');
const path = require('path');

const KEBAB_RE = /^[a-z][a-z0-9]*(-[a-z0-9]+)*$/;

/** Escape HTML special chars for safe interpolation into templates. */
function escapeHtml(str) {
  return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

const INPUT_CSS = `@tailwind base;
@tailwind components;
@tailwind utilities;
`;

function indexHtml(title, description) {
  return `<!DOCTYPE html>
<html lang="en" class="light">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${title} — Mockup</title>
  <link rel="stylesheet" href="./output.css">
  <script defer src="https://cdn.jsdelivr.net/npm/alpinejs@3/dist/cdn.min.js"></script>
</head>
<body class="bg-neutral-50 text-neutral-900 dark:bg-neutral-900 dark:text-neutral-50 min-h-screen flex flex-col">
  <header class="bg-white dark:bg-neutral-800 border-b border-neutral-200 dark:border-neutral-700">
    <div class="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 flex items-center justify-between h-14">
      <a href="./index.html" class="font-bold text-lg">${title}</a>
    </div>
  </header>
  <main class="flex-1 max-w-3xl mx-auto px-4 py-12 w-full">
    <h1 class="text-3xl font-bold mb-2">${title}</h1>
    <p class="text-neutral-600 dark:text-neutral-300 mb-8">${description}</p>
    <p class="text-neutral-400">Pages will be generated by the mockup creator skill.</p>
  </main>
  <footer class="border-t border-neutral-200 dark:border-neutral-700 mt-auto">
    <div class="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-6 text-center text-sm text-neutral-500">
      Mockup &mdash; generated from design system tokens
    </div>
  </footer>
</body>
</html>
`;
}

function toTitleCase(kebab) {
  return kebab.replace(/-/g, ' ').replace(/\b\w/g, c => c.toUpperCase());
}

function parseArgs() {
  const args = process.argv.slice(2);
  const parsed = { name: null, outputDir: 'docs/design-mockups', pages: '', description: '', force: false };
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--name' && args[i + 1]) parsed.name = args[++i];
    else if (args[i] === '--output-dir' && args[i + 1]) parsed.outputDir = args[++i];
    else if (args[i] === '--pages' && args[i + 1]) parsed.pages = args[++i];
    else if (args[i] === '--description' && args[i + 1]) parsed.description = args[++i];
    else if (args[i] === '--force') parsed.force = true;
  }
  if (!parsed.name) {
    console.error('Usage: node scaffold-mockup-flow.js --name <flow-name> [--output-dir <dir>] [--pages "a,b,c"] [--description "..."] [--force]');
    console.error('  --name is required (kebab-case)');
    process.exit(1);
  }
  return parsed;
}

function main() {
  const { name, outputDir, pages: pagesArg, description, force } = parseArgs();

  if (!KEBAB_RE.test(name)) {
    console.error(`Error: flow name "${name}" must be kebab-case (e.g., auth-flow)`);
    process.exit(1);
  }

  const flowDir = path.join(outputDir, name);
  if (fs.existsSync(flowDir) && !force) {
    console.error(`Error: ${flowDir} already exists. Use --force to overwrite.`);
    process.exit(1);
  }

  fs.mkdirSync(flowDir, { recursive: true });

  // Parse and validate page names (must be kebab-case to prevent path traversal)
  const pageNames = pagesArg ? pagesArg.split(',').map(p => p.trim()).filter(Boolean) : [];
  for (const pname of pageNames) {
    if (!KEBAB_RE.test(pname)) {
      console.error(`Error: page name "${pname}" must be kebab-case (e.g., login, forgot-password)`);
      process.exit(1);
    }
  }
  const pages = pageNames.map((pname, i) => ({
    file: `${pname}.html`,
    title: toTitleCase(pname),
    description: '',
    navOrder: i + 1,
  }));

  // Build navigation map (each page links to all others)
  const navigation = {};
  for (const page of pages) {
    navigation[page.file] = pages.filter(p => p.file !== page.file).map(p => p.file);
  }

  const today = new Date().toISOString().split('T')[0];
  const manifest = {
    name,
    description,
    created: today,
    updated: today,
    themes: [],
    pages,
    navigation,
  };

  // Write files
  fs.writeFileSync(path.join(flowDir, 'mockup.json'), JSON.stringify(manifest, null, 2) + '\n', 'utf-8');
  fs.writeFileSync(path.join(flowDir, 'input.css'), INPUT_CSS, 'utf-8');

  const title = escapeHtml(toTitleCase(name));
  const desc = escapeHtml(description || `${title} mockup flow`);
  fs.writeFileSync(path.join(flowDir, 'index.html'), indexHtml(title, desc), 'utf-8');

  console.error(`Scaffolded ${flowDir}/`);
  console.error(`  mockup.json — ${pages.length} page(s)`);
  console.error(`  input.css`);
  console.error(`  index.html`);
}

main();
